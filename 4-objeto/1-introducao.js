//Linguagem procedural
processamento(valor1, valor2, valor3) //A função processa os valores

//OO - Você tem dados, e esses dados manipulam as funções
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        //...
    }
}

objeto.processamento() //Invocando função atraves do dado e o foco passou a ser o objeto

